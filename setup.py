def generate_hardware_id(hardware_info: dict):
    print(hardware_info)

    # 将硬件信息转换为 JSON 字符串
    hardware_json = json.dumps(hardware_info, separators=(',', ':'), ensure_ascii=False)

    # 对 JSON 字符串进行 Base64 编码
    hardware_base64 = base64.b64encode(hardware_json.encode('utf-8')).decode('utf-8')

    # 构造最终的数据对象
    data = {
        "hardware": hardware_base64
    }

    # 将数据对象转换为 JSON 字符串
    data_json = json.dumps(data, separators=(',', ':'), ensure_ascii=False)

    # 计算 SHA-256 哈希值
    sha256_hash = hashlib.sha256(data_json.encode('utf-8')).hexdigest()

    # 输出结果
    print(sha256_hash)
    return sha256_hash

# 读取 JSON 文件
def read_json_file(file_path):
    with open(file_path, 'r') as file:
        return json.load(file)

# 写入 JSON 文件
def write_json_file(file_path, data):
    with open(file_path, 'w') as file:
        json.dump(data, file, indent=4)

# 处理 JSON 数据
def process_json_data(data):
    for account in data:
        for node in account["Nodes"]:
            # 检查是否已经存在 HardwareId 或 HardwareInfo
            if "HardwareId" in node or "HardwareInfo" in node:
                print(f"跳过节点 {node['PubKey']}，已存在 HardwareId 或 HardwareInfo")
                continue  # 跳过当前节点
            
            # 生成硬件信息
            hardware_info = generate_hardware_info()
            # 生成硬件 ID
            hardware_id = generate_hardware_id(hardware_info)
            # 将硬件信息和硬件 ID 添加到节点中
            node["HardwareInfo"] = hardware_info
            node["HardwareId"] = hardware_id
            print(f"已为节点 {node['PubKey']} 生成 HardwareInfo 和 HardwareId")
    return data

# 主函数
def main():
    file_path = 'accounts.json'
    
    # 读取 JSON 文件
    data = read_json_file(file_path)
    
    # 处理 JSON 数据
    updated_data = process_json_data(data)
    
    # 写回 JSON 文件
    write_json_file(file_path, updated_data)
    print(f"数据已更新并写回文件: {file_path}")

